class Address:

    def __init__(self, building_number,
                 street_name,
                 street_type,
                 city,
                 state,
                 postal_code,
                 country):
        self.building_number = building_number
        self.street_name = street_name
        self.street_type = street_type
        self.city = city
        self.state = state
        self.postal_code = postal_code
        self.country = country
